let myDate=new Date()

console.log(myDate.toDateString()) //Mon Jun 30 2025
console.log(myDate.toLocaleDateString())//6/30/2025
console.log(myDate.toString())//Mon Jun 30 2025 04:15:22 GMT+0000 (Coordinated Universal Time)
console.log(myDate.toISOString()) //2025-06-30T04:15:22.829Z
console.log(myDate.toJSON())//2025-06-30T04:17:51.909Z 
console.log(myDate.toLocaleString())//  6/30/2025, 4:15:22 AM
console.log(typeof myDate)//object

